The data from the rolling house prices for Brooklyn is based on the following data:
  Brooklyn Rolling Sales File.  All Sales From September 2015 - August 2016.
  Sales File as of 9/14/2016  Co-op Sales Files as of 9/9/2016
  Neighborhood Names and Descriptive Data as of 5/26/2016
  Building Class Category is based on Building Class at Time of Sale.
  
Data patterns of scatter plots indicate that the log base 10 of houses with 3.0 to 3.5 gross square feet, 
typically have a log base of 10 sale price of between 5.0 and 6.0.  The mean house price excluding any NA values
is 1,267,542 and the mean home square footage excluding the NA values is 4,415.637.